const int MAXM = (int)1e5+100;
const int MAXN = (int)1e5+100;
const int MOD  = (int)1e9+7;

array<int,MAXN>arr , temp;
long long counter{};
void merge(int start , int end){
    int mid = (start+end)/2;
    int i = start , j = mid+1 , k = start;
    while(i<=mid && j<=end){
        if(arr[i]>arr[j]){
            counter+=abs(mid-i+1);
            temp[k++] = arr[j++];
        }else{
            temp[k++] = arr[i++];
        }
    }
    while(i<=mid){
        temp[k++] = arr[i++];
    }
    while(j<=end){
        temp[k++] = arr[j++];
    }
    for(i = start; i<=end; ++i){
        arr[i] = temp[i];
    }
    return ;
}
void mergeSort(int start , int end){
    if(start==end){return;}
    int mid = (start+end)/2;
    mergeSort(start , mid);
    mergeSort(mid+1 , end);
    merge(start , end);
}
